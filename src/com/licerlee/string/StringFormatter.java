package com.licerlee.string;

public class StringFormatter {

	
	public static void main(String[] args) {
		
		// 简单格式化输出
		System.out.printf("Hello, %s, I am %d, and I hava %f money", "liwc", 5, 123f);
		System.out.println();
		// 等价printf()
		System.out.format("Hello, %s, I am %d, and I hava %f money", "liwc", 5, 123f);
		
		
		
		
		
	}
}
